                <h2 class="title">住建部严控开发商违规行为 开发商销售利润或缩水</h2>
                <div class="attr mtl mbl clearfix">
                    <div class="mrl left">
                        <span class="time">发布时间：2016-10-22 11:31</span>
                    </div>
                    <div class="mll mrl left">
                        <span class="author">作者：未知</span>
                    </div>
                    <div class="mll right">
                        <span class="source">来源：每日经济新闻</span>
                    </div>
                </div>
                <p class="summary">
                    <span><b>文章摘要：</b></span>住建部对房地产开发企业的9种不正当经营行为作出了明确界定，“团购费”、“优惠费”、“折扣款”、“电商费”等房价之外的违规费用，被明令禁止。
                </p>
            </div>
            <div class="article-bd mbxxl">
            	<div class="article-detail">
                <p>房地产商巧立各种名目收费的日子一去不复返了。</p>
                <p>近日，住建部发布通知，要求进一步整顿规范房地产市场秩序，并对房地产开发企业的9种不正当经营行为作出了明确界定。</p>
                <p>深圳、上海、武汉、成都等城市先后对房地产市场专项整治活动中的违规企业进行了通报。</p>
                <p>近日，媒体也接连曝光一些涉嫌违规收取“团购费”“电商费”等款项的楼盘。</p>
                <p>据《每日经济新闻》记者调查了解，这些附加费能为开发商节省3%~5%的营销费用，但这类违规的额外费用一经取消，开发商销售利润或将缩水。</p>
                <h1>开发商算得一手好账</h1>
                <p>针对住建部所界定的9种不正当经营行为，有业内人士向记者直言，违法违规销售，会影响市场健康交易，影响房地产企业的口碑；落到每个购房者身上，则会加重购房成本，而且权益得不到保证，滋生的投诉案例会越来越多。</p>
                <p>这种行为对购房者来说是增加成本，对房地产企业来说却是降成本。据某业内资深一线操盘手透露，一般每个楼盘原本就会有几个点的优惠，定下来后，楼盘营销给出的优惠都在里面包装。开发商不会做亏本买卖，甚至连利润都不会真的削减。</p>
                <p>而代理公司的存在，能用各种名义来降低营销费用。“比如购房者通过电商渠道缴纳了电商费，这部分费用看似换取了楼盘的高额度优惠，但其实开发商并没有因为优惠而亏本；另一方面，这部分电商费被代理公司或者操盘的互联网中介收取了，相当于开发商分配给上述合作公司的奖金。如此一来，公司不必出多少营销费用，就可以把房子卖掉。”</p>
                <p>资深地产人士薛建雄表示，以一套总价300万元的房子为例，开发商内部的营销费用差不多是总价的3%~5%，其中中介代理费1%~2%，营销推广费用2%。向消费者收取的电商费或其他名目的类似收费，可以正好抵消代理费用和一部分营销费用。</p>
                <h1>开发商销售利润或缩水</h1>
                <p>“团购费”、“优惠费”、“折扣款”、“电商费”等房价之外的违规费用，被明令禁止。不少业内人士认为，这对开发商来说，可能不是少赚一笔钱的问题。</p>
                <p>薛建雄表示，开发商不敢巧立名目收取各种附加费，公司营运成本会大增。</p>
                <p>如果不能收取电商费用，开发商相应的营销费用等于一下增加3%~5%。如果想要获得原先的推广效果，还需要增加相应的营销费用，如果没有增加，销售实力或将会受到影响。</p>
                <p>伴随开发商营销费用的增加，土地成本和财务成本都在上升，接下来可能导致的，便会是企业利润率的下滑。</p>
                <p>以某上市房企位于上海泗泾的一个楼盘为例。该楼盘单价40000元/平方米（精装修），按8%的净利率水平来计算，单价净利润在3200元左右。据购房者向媒体爆料，该楼盘在首付五成的基础上加收了40万元的装修费。按照100平方米的房屋粗略计算，开发商卖一套房每平方米能多收4000元，扣除10%的企业所得税，每平方米也能多得3600元。从上述单价利润可见，附加费一收，该项目利润近乎翻番。</p>
                <p>目前，在房地产市场秩序整顿过程中，不计入购房款的附加费被取消了，这意味着开发商单盘的销售利润率将直接缩水。</p>
                <p>随着市场供求关系的变化，新城控股高级副总裁欧阳捷认为，“开发商为了抢占份额，或者出现主动降价的情况。部分项目价格下降，开发商的利润会进一步折损。”</p>
                <p>不过，欧阳捷预测，今年很多房企在前10个月已经基本完成全年任务，年内“以价换量冲业绩”的情况不大可能出现，而且结余部分会放到明年，大企业降价的可能性也比较低。</p>
                <p>对于购房者来说，这些附加费用没有了，买房的支出可以少一些，但不少购房者担心，上述电商费等附加费被禁，会否“变形”成其他方式，最终转嫁到消费者身上？</p>
                <p>欧阳捷认为，电商的逻辑是人气越高、成交概率越大，它的最大价值在于带客，还不在于保证成交，但“羊毛出在羊身上”的逻辑依旧，这样的情况可能出现。</p>
                <p>不过，薛建雄认为，现在市场竞争日渐激烈，在房地产向买方市场转变的过程中，大多开发商是不敢大肆提高价格的，因为这会削弱它的市场吸引力和竞争力。</p>
